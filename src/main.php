<?php
//ループ処理
//1.整数 1 ~ 10 を、1 から順に改行区切りで出力
for($i = 0; $i <= 10; $i++){
    echo $i . '\n';
}

//[処理A]の部分に、for文を使って配列$listの全要素を最初から順に出力
$list = [11,22,33,44,55,66];
for($x = 0; $x < count($list); $x++){
    echo $list[$x] . '\n';
}

//九九
for($a = 1; $a <= 9; $a++){
    for($b =1; $b <= 9; $b++){
        echo $a * $b . '\n';
    }
}



?>